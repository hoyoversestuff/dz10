<?php
// функция для проверки на простое число
function isPrime($num) {
    // если число меньше 2, оно не простое
    if ($num < 2) return false;
    // проверяем делители до квадратного корня числа
    for ($i = 2; $i * $i <= $num; $i++) {
        // если делится на i, значит это не простое число
        if ($num % $i == 0) return false;
    }
    return true; // если делителей не найдено, число простое
}

// рисуем таблицу с цветом рамки черный и цветом ячейки белый
echo "<table border='1'>";

// цикл для заполнения самих чисел (от 1 до 10)
for ($i = 1; $i <= 10; $i++) {
    echo "<tr>"; // открываем новую строку в таблице
    // вложенный цикл для столбцов с перемножением чисел
    for ($j = 1; $j <= 10; $j++) {
        $result = $i * $j; // перемножаем
        // проверяем, если число простое, применяем класс "prime"
        if (isPrime($result)) {
            echo "<td class='prime'>$result</td>"; // если простое, выделяем красным
        } else {
            echo "<td class='white'>$result</td>"; // если не простое, оставляем белым
        }
    }
    echo "</tr>"; // закрываем строку таблицы
}

// заканчиваем таблицу
echo "</table>";


?>